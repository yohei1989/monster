function checkUnchiSpawn() {
  const h = new Date().getHours();
  // Check if current hour matches any unchi time that hasn't been flagged
  if (!state._unchiSpawnedHours) state._unchiSpawnedHours = [];
  for (const t of state.unchiTimes) {
    if (h >= t && !state._unchiSpawnedHours.includes(t) && state.unchiCount < 3) {
      state._unchiSpawnedHours.push(t);
      state.unchiCount = Math.min(3, state.unchiCount + 1);
      showSpeech('💩でたよ！', true);
      saveState();
      updateUI();
      break;
    }
  }
}

