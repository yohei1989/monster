function getDayPeriod() {
  const h = new Date().getHours();
  if (h >= 5 && h < 9) return 'morning';
  if (h >= 9 && h < 17) return 'afternoon';
  if (h >= 17 && h < 20) return 'evening';
  if (h >= 20 && h < 23) return 'night';
  return 'midnight';
}

function getDayPeriodIcon() {
  const p = getDayPeriod();
  const icons = { morning: '☀', afternoon: '🌤', evening: '🌆', night: '🌙', midnight: '💤' };
  return icons[p] || '☀';
}

function getIdleMessage() {
  const period = getDayPeriod();
  const creature = CREATURES[state.creatureId];
  const nearEvo = creature && creature.evolvesTo && state.evoPoints >= 70;

  if (nearEvo) return '……なにかが変わりそう';

  const msgs = {
    morning: ['おはよう！', 'きょうも\nよろしく！', 'ねむい…'],
    afternoon: ['ぼーっと\nしてる…', 'ひまだなぁ', 'ねむくなってきた'],
    evening: ['とっくん\nしたいな！', 'がんばるぞ！', 'うごきたい！'],
    night: ['おなかすいた', 'ごはんほしい', 'むぎゅ…'],
    midnight: ['Zzz…', 'すやすや…', 'zzz'],
  };

  const arr = msgs[period] || msgs.afternoon;
  return arr[Math.floor(Date.now() / 8000) % arr.length];
}

