function checkDailyReset() {
  const today = todayKey();
  if (state.lastResetDay !== today) {
    state.lastResetDay = today;
    state.gohanUsed = 0;
    state.tokkunUsed = 0;
    state.unchiCount = 0;
    state.unchiTimes = generateUnchiTimes();
    // Decay stats overnight
    state.fullness = Math.max(10, state.fullness - 30);
    state.happiness = Math.max(10, state.happiness - 10);
    if (state.unchiCount > 0) {
      state.cleanliness = Math.max(10, state.cleanliness - 20);
      state.happiness = Math.max(5, state.happiness - 15);
    }
    saveState();
  }
}

