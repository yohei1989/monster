const PIXEL_SIZE = 7; // Each pixel = 7px on canvas (16*7 = 112, fits 120px)
const CANVAS_W = 120;
const CANVAS_H = 96;

function drawCreature(canvas, creatureId, frame, scale, darkMode) {
  const ctx = canvas.getContext('2d');
  const creature = CREATURES[creatureId];
  if (!creature) return;

  ctx.clearRect(0, 0, canvas.width, canvas.height);

  const grid = creature.frames[frame % creature.frames.length];
  const pixSize = scale || PIXEL_SIZE;
  const gridSize = 16;
  const totalW = gridSize * pixSize;
  const totalH = gridSize * pixSize;
  const offsetX = Math.floor((canvas.width - totalW) / 2);
  const offsetY = Math.floor((canvas.height - totalH) / 2);

  for (let row = 0; row < gridSize; row++) {
    for (let col = 0; col < gridSize; col++) {
      const colorIdx = grid[row][col];
      if (colorIdx === 0) continue;
      const color = LCD_COLORS[colorIdx];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(offsetX + col * pixSize, offsetY + row * pixSize, pixSize, pixSize);
    }
  }
}

