// ============================================
let animFrame = 0;
let animTimer = null;
let frameCounter = 0;
const FRAME_RATE = 60; // ticks between animation frames

function startAnimation() {
  if (animTimer) clearInterval(animTimer);
  animTimer = setInterval(() => {
    frameCounter++;
    if (frameCounter >= FRAME_RATE) {
      frameCounter = 0;
      animFrame = (animFrame + 1) % 2;
      renderCreature();
    }
    updateStatusIcons();
  }, 100);
}

function renderCreature() {
  const canvas = document.getElementById('creature-canvas');
  if (!canvas || !state) return;

  const period = getDayPeriod();
  const sleeping = (period === 'night' || period === 'midnight');

  drawCreature(canvas, state.creatureId, animFrame, PIXEL_SIZE, false);

  // Evolution glow check
  const evoAura = document.getElementById('evo-aura');
  const creature = CREATURES[state.creatureId];
  const nearEvo = creature && creature.evolvesTo && state.evoPoints >= 70;
  evoAura.style.display = nearEvo ? 'block' : 'none';

  // Sleeping overlay
  if (sleeping) {
    const ctx = canvas.getContext('2d');
    ctx.fillStyle = 'rgba(0,10,0,0.5)';
    ctx.fillRect(0, 0, canvas.width, canvas.height);
  }
}

