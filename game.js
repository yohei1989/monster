function checkEvolution() {
  const creature = CREATURES[state.creatureId];
  if (!creature || !creature.evolvesTo) return;

  // Bonus from battle wins
  const battleBonus = Math.min(state.battleWins * 2, 20);
  const totalEvo = state.evoPoints + battleBonus;

  if (totalEvo >= 100) {
    triggerEvolution(creature.evolvesTo);
  }
}

function triggerEvolution(nextId) {
  const nextCreature = CREATURES[nextId];
  if (!nextCreature) return;

  // Evolution sequence
  const device = document.getElementById('device');
  const lcd = document.getElementById('lcd');

  // Phase 1: shake
  device.classList.add('shake');
  setTimeout(() => device.classList.remove('shake'), 500);

  // Phase 2: screen flash / noise
  showSpeech('……！！', true);
  showActionResult('しんか！！');

  lcd.style.animation = 'none';
  setTimeout(() => {
    // Phase 3: evolve
    state.creatureId = nextId;
    state.evoPoints = 0;
    if (!state.discovered.includes(nextId)) {
      state.discovered.push(nextId);
    }
    saveState();
    animFrame = 0;
    renderCreature();
    updateUI();
    showSpeech(`${nextCreature.name}に\nしんかした！`, true);

    // Second shake
    setTimeout(() => {
      device.classList.add('shake');
      setTimeout(() => device.classList.remove('shake'), 500);
    }, 200);
  }, 1500);
}

