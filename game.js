function defaultState() {
  const types = Object.keys(STARTER_BY_TYPE);
  const randomType = types[Math.floor(Math.random() * types.length)];
  const starterId = STARTER_BY_TYPE[randomType];
  const charId = generateCharId();

  return {
    creatureId: starterId,
    charId: charId,
    birthday: Date.now(),
    lastSeen: Date.now(),
    // Hidden stats (not shown to user)
    health: 100,
    fullness: 80,
    happiness: 70,
    cleanliness: 100,
    evoPoints: 0,
    battleWins: 0,
    battleCount: 0,
    // Daily limits
    gohanUsed: 0,
    tokkunUsed: 0,
    unchiCount: 0,
    lastResetDay: todayKey(),
    // Discovery
    discovered: [starterId],
    // Battle history
    battleHistory: [],
    // Random seeds for unchi timing
    unchiTimes: generateUnchiTimes(),
  };
}

function generateCharId() {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  let id = '';
  for (let i = 0; i < 6; i++) id += chars[Math.floor(Math.random() * chars.length)];
  return id;
}

function todayKey() {
  const d = new Date();
  return `${d.getFullYear()}-${d.getMonth()}-${d.getDate()}`;
}

function generateUnchiTimes() {
  // 1-3 random times during the day (as hours 8-22)
  const count = 1 + Math.floor(Math.random() * 3);
  const times = [];
  for (let i = 0; i < count; i++) {
    times.push(8 + Math.floor(Math.random() * 14));
  }
  return times.sort((a, b) => a - b);
}

function loadState() {
  try {
    const raw = localStorage.getItem(SAVE_KEY);
    if (raw) {
      state = JSON.parse(raw);
      // Migrate missing fields
      if (!state.charId) state.charId = generateCharId();
      if (!state.discovered) state.discovered = [state.creatureId];
      if (!state.battleHistory) state.battleHistory = [];
      if (!state.unchiTimes) state.unchiTimes = generateUnchiTimes();
    } else {
      state = defaultState();
    }
  } catch (e) {
    state = defaultState();
  }
}

function saveState() {
  try {
    state.lastSeen = Date.now();
    localStorage.setItem(SAVE_KEY, JSON.stringify(state));
  } catch (e) {}
}

