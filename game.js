function initEvents() {
  // D-PAD buttons
  document.getElementById('btn-gohan').addEventListener('click', () => {
    if (!document.getElementById('btn-gohan').classList.contains('disabled')) doGohan();
  });
  document.getElementById('btn-souji').addEventListener('click', () => {
    if (!document.getElementById('btn-souji').classList.contains('disabled')) doSouji();
  });
  document.getElementById('btn-tokkun').addEventListener('click', () => {
    if (!document.getElementById('btn-tokkun').classList.contains('disabled')) doTokkun();
  });
  document.getElementById('btn-battle').addEventListener('click', () => {
    buildBattleHistory();
    document.getElementById('tab-challenge').classList.add('active');
    document.getElementById('tab-history').classList.remove('active');
    document.getElementById('tab-content-challenge').style.display = 'block';
    document.getElementById('tab-content-history').style.display = 'none';
    showModal('modal-battle');
  });

  // Right buttons
  document.getElementById('btn-zukan').addEventListener('click', () => {
    buildZukan();
    showModal('modal-zukan');
  });
  document.getElementById('btn-share').addEventListener('click', () => {
    buildSharePreview();
    showModal('modal-share');
  });

  // Battle tabs
  document.getElementById('tab-challenge').addEventListener('click', () => {
    document.getElementById('tab-challenge').classList.add('active');
    document.getElementById('tab-history').classList.remove('active');
    document.getElementById('tab-content-challenge').style.display = 'block';
    document.getElementById('tab-content-history').style.display = 'none';
  });
  document.getElementById('tab-history').addEventListener('click', () => {
    document.getElementById('tab-history').classList.add('active');
    document.getElementById('tab-challenge').classList.remove('active');
    document.getElementById('tab-content-history').style.display = 'block';
    document.getElementById('tab-content-challenge').style.display = 'none';
    buildBattleHistory();
  });

  // Do battle
  document.getElementById('do-battle-btn').addEventListener('click', () => {
    const input = document.getElementById('battle-id-input').value.trim().toUpperCase();
    if (input.length < 4) {
      showSpeech('IDを入力して！', true);
      return;
    }
    if (input === state.charId) {
      showSpeech('じぶんとは\nたたかえない！', true);
      return;
    }
    hideModal();
    setTimeout(() => doBattle(input), 200);
  });

  // Copy ID buttons
  document.getElementById('copy-id-btn').addEventListener('click', () => {
    copyToClipboard(state.charId, document.getElementById('copy-id-btn'));
  });
  document.getElementById('copy-share-id-btn').addEventListener('click', () => {
    copyToClipboard(state.charId, document.getElementById('copy-share-id-btn'));
  });

  // Close modals
  document.getElementById('close-battle').addEventListener('click', hideModal);
  document.getElementById('close-zukan').addEventListener('click', hideModal);
  document.getElementById('close-result').addEventListener('click', hideModal);
  document.getElementById('close-share').addEventListener('click', hideModal);
  document.getElementById('modal-overlay').addEventListener('click', e => {
    if (e.target === document.getElementById('modal-overlay')) hideModal();
  });
}

