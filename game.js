function getCharacterPower(s) {
  const creature = CREATURES[s.creatureId];
  if (!creature) return 0;
  const stageMult = [1, 1.5, 2, 3][creature.stage] || 1;
  const winBonus = Math.min(s.battleWins || 0, 20) * 2;
  const evoBonus = (s.evoPoints || 0) * 0.3;
  const statsScore = ((s.health || 50) + (s.happiness || 50) + (s.cleanliness || 50)) / 3;
  return Math.floor((stageMult * 50 + statsScore + winBonus + evoBonus));
}

function doBattle(opponentId) {
  // Load opponent from localStorage simulation
  // In real usage, opponent data would come from a server
  // Here we simulate by generating a character from the ID seed
  const opponent = simulateOpponent(opponentId);
  if (!opponent) {
    showModalResult('エラー', `<p class="result-display">キャラID「${opponentId}」が見つかりません。<br><br>正しいIDか確認してください。</p>`);
    return;
  }

  const myPower = getCharacterPower(state);
  const oppPower = getCharacterPower(opponent);

  // Add randomness ±15%
  const myRoll = myPower * (0.85 + Math.random() * 0.3);
  const oppRoll = oppPower * (0.85 + Math.random() * 0.3);

  const win = myRoll > oppRoll;

  const myCreature = CREATURES[state.creatureId];
  const oppCreature = CREATURES[opponent.creatureId] || { name: '???', typeIcon: '?' };

  // Record
  const record = {
    date: new Date().toLocaleDateString('ja-JP'),
    oppId: opponentId,
    oppName: oppCreature.name,
    myName: myCreature.name,
    win: win,
  };
  state.battleHistory.unshift(record);
  if (state.battleHistory.length > 20) state.battleHistory = state.battleHistory.slice(0, 20);
  state.battleCount = (state.battleCount || 0) + 1;

  if (win) {
    state.battleWins = (state.battleWins || 0) + 1;
    state.evoPoints = Math.min(99, state.evoPoints + 5);
    showSpeech('かった！！', true);
  } else {
    state.happiness = Math.max(0, state.happiness - 10);
    showSpeech('まけた…', true);
  }

  saveState();
  updateUI();
  checkEvolution();

  // Show result
  const resultHtml = `
    <div class="${win ? 'result-win' : 'result-lose'}">${win ? '🏆 WINNER!' : '💀 LOSE'}</div>
    <br>
    <div style="font-size:5px;color:#9a8870;line-height:2">
      ${myCreature.name} (${myCreature.typeIcon})<br>
      パワー: ${Math.floor(myRoll)}<br>
      <br>
      vs<br>
      <br>
      ${oppCreature.name} (${oppCreature.typeIcon})<br>
      パワー: ${Math.floor(oppRoll)}
    </div>
  `;

  document.getElementById('result-title').textContent = win ? '🏆 WIN!' : '💀 LOSE...';
  document.getElementById('result-display').innerHTML = resultHtml;
  showModal('modal-result');
}

function simulateOpponent(idStr) {
  // Generate a deterministic opponent from ID string
  // This allows cross-player battles via shared IDs
  // In a real app, this would be a server lookup
  
  // Check localStorage for saved shared data
  try {
    const sharedKey = `pc_shared_${idStr}`;
    const raw = localStorage.getItem(sharedKey);
    if (raw) return JSON.parse(raw);
  } catch (e) {}

  // If ID is 6 chars (valid format), generate from seed
  if (!/^[A-Z2-9]{6}$/.test(idStr.toUpperCase())) return null;

  const seed = idStr.split('').reduce((a, c) => a + c.charCodeAt(0), 0);
  const types = Object.keys(STARTER_BY_TYPE);
  const type = types[seed % types.length];
  const allCreatures = Object.keys(CREATURES).filter(k => CREATURES[k].type === type);
  const stage = Math.floor(seed / 100) % 4;
  const stageCreatures = allCreatures.filter(k => CREATURES[k].stage <= stage);
  const creatureId = stageCreatures[seed % stageCreatures.length] || allCreatures[0];

  return {
    creatureId: creatureId,
    charId: idStr,
    evoPoints: (seed * 7) % 100,
    battleWins: (seed * 3) % 15,
    health: 50 + (seed % 50),
    happiness: 40 + (seed % 60),
    cleanliness: 50 + (seed % 50),
  };
}

