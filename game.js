// BATTLE HISTORY UI
// ============================================
function buildBattleHistory() {
  const el = document.getElementById('battle-history-list');
  if (!state.battleHistory.length) {
    el.innerHTML = '<p class="modal-hint">まだバトル履歴がありません</p>';
    return;
  }
  el.innerHTML = state.battleHistory.map(r => `
    <div class="battle-record ${r.win ? 'win' : 'lose'}">
      ${r.date} ${r.win ? '🏆 WIN' : '💀 LOSE'}<br>
      ${r.myName} vs ${r.oppName} (ID: ${r.oppId})
    </div>
  `).join('');
}

// ============================================
// COPY TO CLIPBOARD
// ============================================
function copyToClipboard(text, btn) {
  navigator.clipboard.writeText(text).then(() => {
    const orig = btn.textContent;
    btn.textContent = 'コピー済！';
    setTimeout(() => { btn.textContent = orig; }, 1500);
  }).catch(() => {
    // Fallback
    const ta = document.createElement('textarea');
    ta.value = text;
    document.body.appendChild(ta);
    ta.select();
    document.execCommand('copy');
    document.body.removeChild(ta);
    const orig = btn.textContent;
    btn.textContent = 'コピー済！';
    setTimeout(() => { btn.textContent = orig; }, 1500);
  });
}

